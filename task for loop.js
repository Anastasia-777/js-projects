/* Задача 12. Составьте массив дней недели. С помощью цикла for выведите все дни недели, а текущий день выведите курсивом.
Текущий день должен храниться в переменной day */

let week = ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun'];
